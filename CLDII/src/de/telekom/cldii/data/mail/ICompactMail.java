package de.telekom.cldii.data.mail;

import java.util.Date;

/**
 * Access to fields of a mail.
 * 
 * @author Anton Wolf, jambit GmbH
 */
public interface ICompactMail {

    /**
     * @return the unique id of this mail
     */
    public String getId();

    /**
     * @return the mail adress from the sender of this mail
     */
    public String getFromAdress();

    /**
     * @return date this mail was recived
     */
    public Date getRecievedDate();

    /**
     * @return subject of the mail
     */
    public String getSubject();

    /**
     * @return true if this mail has been answered
     */
    public boolean answered();

    /**
     * @return true if this mail has been read
     */
    public boolean read();
}

package de.telekom.cldii.view.util;

/**
 * Listener for {@link FlingGestureDetector} with callback methods
 * 
 * @author Jun Chen, jambit GmbH
 * 
 */
public interface OnFlingListener {

    /**
     * This method is called when the user flings from right to left
     */
    public void onLeftFling();

    /**
     * This method is called when the user flings form left to right
     */
    public void onRightFling();
}

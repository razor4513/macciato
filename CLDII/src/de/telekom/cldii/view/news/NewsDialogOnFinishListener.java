package de.telekom.cldii.view.news;

/**
 * This interface includes the callback method to dismiss a shown alert dialog
 * and then refreshing the view on demand
 * 
 * @author Jun Chen, jambit GmbH
 * 
 */
public interface NewsDialogOnFinishListener {

    /**
     * Dismisses a showing {@link AlertDialog} and updates the content of the
     * grid view if desired
     * 
     * @param refreshView
     *            true if you want the grid view to refresh, false otherwise
     */
    public void dismissDialog(boolean refreshView);

}

package de.telekom.cldii.service.imagedownload;

/**
 * Object that holds a necessary information for the RSSImageRetriever
 * 
 * @author Jun Chen, jambit GmbH
 * 
 */
public class RSSImageRetrieverItem {

    private final String broadcastId;
    
    private final String broadcastInfo;

    private final String url;
    
    private final boolean isThumbnail;

    public RSSImageRetrieverItem(String broadcastId, String broadcastInfo, String url, boolean isThumbnail) {
        this.broadcastId = broadcastId;
        this.broadcastInfo = broadcastInfo;
        this.url = url;
        this.isThumbnail = isThumbnail;
    }

    public String getBroadcastId() {
        return this.broadcastId;
    }
    
    public String getBroadcastInfo() {
        return broadcastInfo;
    }

    public String getUrl() {
        return url;
    }
    
    public boolean isThumbnail() {
        return isThumbnail;
    }

}
